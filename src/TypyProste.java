public class TypyProste {
    // liczby staloprzecinkowe
    byte one = 127; // 1 bajt od -128 do 127
    short two = 3689; // 2 bajty od -32768 do 32767
    int three = 23290932; // 4 bajty od - 2 147 483 648 do 2 147 483 647
    long four = 2353235423L; // 8 bajtów, bardzo duże liczby, należy deklarować longa literką L na koniec cyfry

    //Liczby zmiennoprzecinkowe
    float five = 4.999999f; // max ok 6-7 liczb po przecinku, też deklarujemy literką f na końcu
    double six = 3.9999999999999999d; // max ok 15 cyfr po przecinku, też deklarujemy na koniec literką d

    // wartości logiczne
    boolean prawda = true;
    boolean fałsz = false;

    // pojedyńczy znak
    char letter = 'a';

}
